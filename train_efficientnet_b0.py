def _oci_bin():
    """Find oci binary, checking common install locations."""
    import shutil, os
    oci = shutil.which("oci")
    if oci:
        return oci
    for p in [
        os.path.expanduser("~/.local/bin/oci"),
        os.path.expanduser("~/bin/oci"),
        "/usr/local/bin/oci",
    ]:
        if os.path.isfile(p):
            return p
    return "oci"  # last resort — let it fail with a clear error

def oracle_upload(local_path: str, object_name: str):
    """Upload a file to Oracle Object Storage with parallel multipart upload."""
    env = os.environ.copy()
    env["PATH"] = os.path.expanduser("~/.local/bin") + ":" + env.get("PATH", "")
    result = subprocess.run([
        _oci_bin(), "os", "object", "put",
        "--namespace", OCI_NAMESPACE,
        "--bucket-name", OCI_BUCKET,
        "--name", object_name,
        "--file", local_path,
        "--force",
        "--parallel-upload-count", "8",   # 8 parallel threads
        "--part-size", "128",              # 128MB parts
    ], env=env, capture_output=True, text=True)
    if result.returncode != 0:
        raise RuntimeError(f"oci put failed: {result.stderr.strip()}")
    print(f"  [oracle] uploaded → {OCI_BUCKET}/{object_name}")

def oracle_upload_bg(local_path: str, object_name: str):
    """Fire-and-forget background upload — training continues immediately."""
    import threading
    def _run():
        try:
            oracle_upload(local_path, object_name)
        except Exception as e:
            print(f"  [oracle-bg] WARNING: {object_name} upload failed — {e}", flush=True)
    t = threading.Thread(target=_run, daemon=True)
    t.start()
    print(f"  [oracle-bg] uploading in background → {object_name}", flush=True)
    return t

def oracle_delete(object_name: str):
    """Delete an object from Oracle Object Storage."""
    subprocess.run([
        "oci", "os", "object", "delete",
        "--namespace", OCI_NAMESPACE,
        "--bucket-name", OCI_BUCKET,
        "--object-name", object_name,
        "--force"
    ], check=True)
    print(f"  [oracle] deleted  → {OCI_BUCKET}/{object_name}")


def find_max_batch_size(model, device, amp_dtype, num_classes, start=32, max_batch=512):
    """Probe VRAM with a dummy forward+backward to find the largest safe batch size."""
    import gc
    ce = nn.CrossEntropyLoss()
    model.train()
    best = start
    batch = start
    print(f"\n  [batch-tuner] Probing VRAM  start={start}  cap={max_batch}", flush=True)
    while batch <= max_batch:
        try:
            torch.cuda.empty_cache(); gc.collect()
            x = torch.zeros(batch, 3, 224, 224, device=device)
            y = torch.zeros(batch, dtype=torch.long, device=device)
            with torch.amp.autocast("cuda", dtype=amp_dtype):
                out = model(x)
                loss = ce(out, y)
            loss.backward()
            model.zero_grad(set_to_none=True)
            del x, y, out, loss
            torch.cuda.empty_cache(); gc.collect()
            best = batch
            print(f"  [batch-tuner]   {batch:>4} ✓", flush=True)
            batch = batch * 2
        except torch.cuda.OutOfMemoryError:
            torch.cuda.empty_cache(); gc.collect()
            print(f"  [batch-tuner]   {batch:>4} OOM → stop", flush=True)
            break
    # 80% of max, aligned to multiple of 8 for tensor-core efficiency
    safe = max(8, (int(best * 0.80) // 8) * 8)
    print(f"  [batch-tuner] Max={best}  Safe(80%)={safe}\n", flush=True)
    return safe


